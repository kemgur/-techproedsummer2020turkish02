package day05incrementdecrement;

public class Concatenation {

	public static void main(String[] args) {
		//Concatenate (Konkateneyt): Birbirine baglamak(Fiil); Concatenation (konketeneysin) = Birbirini baglayis(isim)
		// Stringler icin kullanilir
		
		String s1 = "Java";//"Java " seklinde de bosluk olusturabiliriz
		String s2 = "Kolaydir";
		System.out.println(s1 + s2);//JavaKolaydir
		System.out.println(s1 + " "+ s2);//Java Kolaydir
		
		String s3 = "Ali ";
		String s4 = "Candir";
		System.out.println(s3 + s4);//Ali Candir 
		
		
		
	}

}
