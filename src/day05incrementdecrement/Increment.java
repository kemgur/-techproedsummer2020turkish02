package day05incrementdecrement;

public class Increment {

	public static void main(String[] args) {
		// increase: Artirmak(fiil)  Increment : artis (isim )
		//1.yol
		int s = 12;
		System.out.println(s);//12
		s = s +3;
		System.out.println(s);//15
		
		//2.yol
		int t =22;
		System.out.println(t);//22;
		t =t + 3; //t+=3 ayni anlama gelir
		System.out.println(t);
		
		//3.yol => Sadece bir artiracaksaniz asagidaki methodu kullanin
		
		int u =32;
		System.out.println(u);//32
		// u =u=1;  veya u+=1; seklinde yazabiliriz
		u++; // seklinde de yazabiliriz. Yaygin olan budur.//33
		System.out.println(u);
		
		
		
		
	}

}
 